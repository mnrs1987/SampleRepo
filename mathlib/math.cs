using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace mathlib
{
    public class math
    {
        public int Add(int val1 , int val2)
        {
            return val1 + val2;
        }
        public int Subtract(int val1, int val2)
        {
            return val1 - val2;
        }
        public int Multiply(int val1, int val2)
        {
            return val1 * val2;
        }
        public int Divide(int val1, int val2)
        {
            return val1 / val2;
        }

    }
}
